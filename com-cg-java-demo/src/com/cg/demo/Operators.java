package com.cg.demo;

public class Operators {
	public static void main(String[] args) {
		int num1 = 25, num2 = 30;
		System.out.println(num1 == num2);
		System.out.println(num1 != num2);
		System.out.println(num1 > num2);
		System.out.println(num1 < num2);
		System.out.println(num1 <= num2);
		System.out.println(num1 >= num2);
		
		
	}

}
